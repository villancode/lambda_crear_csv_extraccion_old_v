info_fuera_cat = """SELECT
TAFC.ID_ACTA_FUERA_CATALOGO AS ID_ACTA,
TAFC.ID_ESTADO,
CE.NOMBRE_ESTADO AS ESTADO,
TAFC.ID_DISTRITO,
CDF.CABECERA_DISTRITAL_FEDERAL AS DISTRITO_FEDERAL,
TAFC.SECCION,
TAFC.ID_CASILLA,
TAFC.TIPO_CASILLA,
TAFC.EXT_CONTIGUA,
'3' as UBICACION_CASILLA,
TAFC.TIPO_DIGITALIZACION,
TAFC.ID_TIPO_CANDIDATURA,
'Fuera de Cat√°logo' AS OBSERVACIONES,
'-' AS LISTA_NOMINAL_CASILLA,
'-' AS PUBLICACION,
'2' AS CONTABILIZA,
'-' AS CLAVE_CASILLA,
'-' AS CLAVE_ACTA,
'-' AS SHA,
'-' AS FECHA_HORA_ACOPIO,
'-' AS FECHA_HORA_CAPTURA,
'-' AS FECHA_HORA_VERIFICACION,
CONCAT(TAFC.ID_TIPO_CANDIDATURA, TAFC.ID_ESTADO, TAFC.ID_DISTRITO) AS estado_distrito,
IFNULL(CDR.NOMBRE, '-') AS TIPO_DOCUMENTO,
TAFC.ID_PROCESO_ELECTORAL
FROM PREP.TR_ACTAS_FUERA_CATALOGO AS TAFC
JOIN PREP.C_ESTADOS CE ON TAFC.ID_ESTADO = CE.ID_ESTADO
JOIN PREP.C_DISTRITOS_FEDERALES CDF ON TAFC.ID_ESTADO = CDF.ID_ESTADO AND TAFC.ID_DISTRITO = CDF.ID_DISTRITO_FEDERAL
LEFT JOIN PREP.C_DOCUMENTOS_RECIBIDOS CDR ON TAFC.ID_DOCUMENTO_RECIBIDO = CDR.ID_DOCUMENTO_RECIBIDO
                AND TAFC.ID_PROCESO_ELECTORAL = CDR.ID_PROCESO_ELECTORAL
;"""

